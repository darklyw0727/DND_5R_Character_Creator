// 反向映射：職業 → 法術名稱陣列
function buildClassSpellLists(): Record<string, string[]> {
  const result: Record<string, string[]> = {}
  for (const [spellName, classes] of Object.entries(SPELL_CLASS_MAP)) {
    for (const cls of classes) {
      if (!result[cls]) result[cls] = []
      result[cls].push(spellName)
    }
  }
  return result
}

export const CLASS_SPELL_LISTS: Record<string, string[]> = buildClassSpellLists()
